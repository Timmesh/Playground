# Lambda Function to find the sum of two given numbers
add = lambda a, b: a + b

print("The Sum of 10, 20 is:", add(10, 20))  # The Sum of 10, 20 is: 30
print("The Sum of 100, 200 is:", add(100, 200))  # The Sum of 100, 200 is: 300

# Explanation:
# - This section defines a lambda function 'add' that calculates the sum of two given numbers.
# - Example usages demonstrate the calculation of sums for pairs of numbers (10, 20) and (100, 200).
