# Function to print a wish message by name
def wish(name):
    print("Hello", name, "Good Morning")

# Function to calculate and print the square of a number
def squareIt(number):
    print("The Square of", number, "is", number * number)

# Calling the 'wish' function with different names
wish("Durga")  # Output: Hello Durga Good Morning
wish("Ravi")   # Output: Hello Ravi Good Morning

# Calling the 'squareIt' function with different numbers
squareIt(4)  # Output: The Square of 4 is 16
squareIt(5)  # Output: The Square of 5 is 25
