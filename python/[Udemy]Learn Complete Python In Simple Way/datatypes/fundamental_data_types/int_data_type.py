# Integer in decimal representation
a = 10
print(a)  # Output: 10

# Integer in octal representation (prefix '0o' or '0O')
b = 0o10
print(b)  # Output: 8

# Integer in hexadecimal representation (prefix '0x' or '0X')
c = 0X10
print(c)  # Output: 16

# Integer in binary representation (prefix '0b' or '0B')
d = 0B10
print(d)  # Output: 2
