# Assigning the boolean value True to variable 'b'
b = True
print(type(b))  # Output: <class 'bool'>

# Assigning values to variables 'a' and 'b'
a = 10
b = 20

# Comparing 'a' and 'b' and assigning the result to variable 'c'
c = a < b
print(c)  # Output: True (because 10 is less than 20)

# Performing arithmetic operations with boolean values
print(True + True)  # Output: 2 (True is treated as 1, so 1 + 1 = 2)
print(True - False)  # Output: 1 (True is treated as 1, and False as 0, so 1 - 0 = 1)
