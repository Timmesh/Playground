# Single-quoted string
s1 = 'Nakul'
print(s1)  # Output: Nakul

# Double-quoted string
s1 = "Nakul"
print(s1)  # Output: Nakul

# Multi-line string using triple single quotes
s1 = '''Nakul
Timmesh'''
print(s1)
# Output:
# Nakul
# Timmesh

# Multi-line string using triple double quotes
s1 = """Nakul
Timmesh"""
print(s1)
# Output:
# Nakul
# Timmesh

# Single-quoted and double-quoted string combination using triple single quotes
s1 = ''''Nakul' "Timmesh"'''
print(s1)
# Output:
# 'Nakul' "Timmesh'"
