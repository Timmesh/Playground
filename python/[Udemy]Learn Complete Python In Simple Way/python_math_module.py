# Mathematical Functions using the math Module

# The math module in Python provides various functions to perform mathematical operations.
# To use any module in Python, we first have to import that module. Here, we are importing the math module.

import math

# Example 1: Using math.sqrt(x)
# The math.sqrt(x) function returns the square root of the given number x.
# Let's find the square root of 16.

print(math.sqrt(16))  # Output: 4.0

# Example 2: Using math.pi
# The math.pi variable represents the value of pi (Ï€).
# Let's print the value of pi.

print(math.pi)  # Output: 3.141592653589793

# The math module contains many other functions that we can use for various mathematical operations.

# Example 3: Using math.ceil(x)
# The math.ceil(x) function returns the smallest integer greater than or equal to x.
# Let's find the ceiling value of 5.7.

print(math.ceil(5.7))  # Output: 6

# Example 4: Using math.floor(x)
# The math.floor(x) function returns the largest integer less than or equal to x.
# Let's find the floor value of 5.7.

print(math.floor(5.7))  # Output: 5

# Example 5: Using math.pow(x, y)
# The math.pow(x, y) function returns x raised to the power of y.
# Let's find 2 raised to the power of 3.

print(math.pow(2, 3))  # Output: 8.0

# Example 6: Using math.factorial(x)
# The math.factorial(x) function returns the factorial of x, which is the product of all positive integers up to x.
# Let's find the factorial of 5.

print(math.factorial(5))  # Output: 120

# Example 7: Using math.trunc(x)
# The math.trunc(x) function returns the truncated value of x (i.e., the integer part without the fractional part).
# Let's find the truncated value of 5.7.

print(math.trunc(5.7))  # Output: 5

# Example 8: Using math.gcd(x, y)
# The math.gcd(x, y) function returns the greatest common divisor (GCD) of x and y.
# Let's find the GCD of 24 and 36.

print(math.gcd(24, 36))  # Output: 12

# There are many more functions and constants available in the math module that can be used for various mathematical calculations.
