# Convert decimal 15 to binary representation
binary_15 = bin(15)
print("Binary representation of 15:", binary_15)  # Output: '0b1111'

# Convert octal 0o11 (decimal 9) to binary representation
binary_octal_11 = bin(0o11)
print("Binary representation of octal 0o11:", binary_octal_11)  # Output: '0b1001'

# Convert hexadecimal 0X10 (decimal 16) to binary representation
binary_hex_10 = bin(0X10)
print("Binary representation of hexadecimal 0X10:", binary_hex_10)  # Output: '0b10000'

# Convert decimal 10 to octal representation
octal_10 = oct(10)
print("Octal representation of 10:", octal_10)  # Output: '0o12'

# Convert binary 0B1111 (decimal 15) to octal representation
octal_binary_1111 = oct(0B1111)
print("Octal representation of binary 0B1111:", octal_binary_1111)  # Output: '0o17'

# Convert hexadecimal 0X123 (decimal 291) to octal representation
octal_hex_123 = oct(0X123)
print("Octal representation of hexadecimal 0X123:", octal_hex_123)  # Output: '0o443'

# Convert decimal 100 to hexadecimal representation
hexadecimal_100 = hex(100)
print("Hexadecimal representation of 100:", hexadecimal_100)  # Output: '0x64'

# Convert binary 0B111111 (decimal 63) to hexadecimal representation
hexadecimal_binary_111111 = hex(0B111111)
print("Hexadecimal representation of binary 0B111111:", hexadecimal_binary_111111)  # Output: '0x3f'

# Convert octal 0o12345 (decimal 5349) to hexadecimal representation
hexadecimal_octal_12345 = hex(0o12345)
print("Hexadecimal representation of octal 0o12345:", hexadecimal_octal_12345)  # Output: '0x14e5'
