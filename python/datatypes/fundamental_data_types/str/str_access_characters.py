# A string containing the text 'NAKUL'
s = 'NAKUL'

# Printing the entire string
print(s)  # Output: NAKUL

# Accessing the character at index 0 (first character)
print(s[0])  # Output: N

# Accessing the character at index 2 (third character)
print(s[2])  # Output: K

# Accessing the character at index -1 (last character, using negative indexing)
print(s[-1])  # Output: L

